package org.basic;

public class FibonacciSeries {

	private void class1() {

		System.out.println("VikRaM");

	}

	private void class2() {

		System.out.println("DillI");

	}

	private void class3() {

		System.out.println("RoleX");

	}

	private void class4() {

		System.out.println("LEO");

	}

	public static void main(String[] args) {
		FibonacciSeries fab = new FibonacciSeries();

		fab.class1();
		fab.class2();
		fab.class3();
		fab.class4();

	}

}
