package org.basic;

public class FibonacciSeries {

	private void class1() {

		System.out.println("Ji");

	}

	private void class2() {

		System.out.println("Manakatha");

	}

	private void class3() {

		System.out.println("Billa");

	}

	private void class5() {
		System.out.println("DarkDevil");
	}

	public static void main(String[] args) {
		FibonacciSeries fab = new FibonacciSeries();

		fab.class1();
		fab.class2();
		fab.class3();
		fab.class5();

	}

}
